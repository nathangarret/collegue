# Laço While - Segue uma estrutura comum ao IF
# while <condiçao> - Quando for false ela para

num = 7


while num <= 49:
    num+=7

print(num) # Gerou de 7 em 7 até um número maior q 49

# útil: 
#   - quando eu não tenho ideia a sequencia que eu desejo iterar
#   - quando eu não tenho ideia de quantas vezes deve ser iterado